struct node
{
  std::variant<int, node_type> type;
  std::array<double,3> frac_coord;
  euler_angles orientation;

  node_type get_blueprint() const
  {
    if(std::holds_alternative<int>(type))
    {
      return building_blocks::predefined_node_types.at(std::get<int>(type));
    }
    else if(std::holds_alternative<node_type>(type))
    {
      return std::get<node_type>(type);
    }
    else
    {
      throw std::runtime_error("Unknown error");
    }
  }
};

