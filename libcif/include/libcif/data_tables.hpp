inline std::size_t get_max_valence(std::string const& name)
{
	std::size_t max_valence;
	if(valence_numbers.count(name) == 0) { max_valence = 10; }
	else { max_valence = valence_numbers[name].back(); }

	return max_valence;
}

inline bool valence_is_valid(std::string const& name, std::size_t valence)
{
  if(valence_numbers.count(name) == 0) { return true; }
  else
  {
    return std::find(std::begin(valence_numbers[name]), std::end(valence_numbers[name]), valence) != std::end(valence_numbers[name]);
  }
}

inline double get_vdw_radius(std::string const& name, std::string const& method)
{
	double vdw_radius = 0;

	if(method == "normal") { vdw_radius = vdw_radii_table[name].vdw_radius; }
	else if(method == "uff") { vdw_radius = vdw_radii_table[name].vdw_radius_uff; }
	else if(method == "mm3") { vdw_radius = vdw_radii_table[name].vdw_radius_mm3; }
	else if(method == "alvarez") { vdw_radius = vdw_radii_table[name].vdw_radius_alvarez; }
	else { throw std::runtime_error("Wrong radius method name!"); }

	if(vdw_radius <= 0) { throw std::runtime_error("This atom doesn't have selected vdw radius:" + name); }

	return vdw_radius;
}

inline double get_cov_radius(std::string const& name, std::string const& strategy)
{
  double cov_radius = 0;

  if(strategy == "cordero") { cov_radius = cov_radii_table[name].covalent_radius_cordero; }
  else if (strategy == "pyykko") { cov_radius = cov_radii_table[name].covalent_radius_pyykko; }
  else if (strategy == "jmol") { cov_radius = cov_radii_table[name].covalent_radius_openbabel; }
  else if (strategy == "pyykko_double") { cov_radius = cov_radii_table[name].covalent_radius_pyykko_double; }
  else if (strategy == "pyykko_triple") { cov_radius = cov_radii_table[name].covalent_radius_pyykko_triple; }
  else { throw std::runtime_error("Wrong radius method name: " + strategy); }

  if(cov_radius <= 0) { throw std::runtime_error("This atom doesn't have selected covalent radius: " + name); }

  return cov_radius / 100;
}

inline bool is_bonded(std::string const& name1, std::string const& name2, double r, std::string const& strategy = "cordero", double tolerance = 1.0)
{
  double cov_rad_1 = get_cov_radius(name1, strategy);
  double cov_rad_2 = get_cov_radius(name2, strategy);

  double sum = cov_rad_1 + cov_rad_2;
  if(strategy == "jmol")
  {
    if((name1=="H" or name2=="H") and (name1=="P" or name2=="P")) { sum += 1.0; }
  }

  if(tolerance > 0) { return r < sum*tolerance; }
  else { return r < sum - tolerance; }
}

inline constexpr double apply_tolerance(double value, double tolerance)
{
  if(tolerance > 0) { return value * tolerance; }
  return value - tolerance;
}

inline bool is_bonded(double cov_rad_1, double cov_rad_2, double r, double tolerance = 1.0)
{
  double sum = cov_rad_1 + cov_rad_2;
  return r < apply_tolerance(sum, tolerance);
}

